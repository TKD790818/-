def ticker_name_map(config: dict[str, Any]) -> dict[str, str]:
    return DEFAULT_TICKER_NAMES | config.get("ticker_names", {})


def ticker_group_map(config: dict[str, Any]) -> dict[str, str]:
    groups = {
        ticker: group
        for group, tickers in DEFAULT_STOCK_GROUPS.items()
        for ticker in tickers
    }
    for group, tickers in config.get("stock_groups", {}).items():
        for ticker in tickers:
            groups[str(ticker)] = str(group)
    return groups | config.get("ticker_groups", {})


def add_stock_names(frame: pd.DataFrame, config: dict[str, Any]) -> pd.DataFrame:
    if "ticker" not in frame.columns:
        return frame

    enriched = frame.copy()
    names = ticker_name_map(config)
    groups = ticker_group_map(config)
    enriched["stock_name"] = enriched["ticker"].map(names).fillna(enriched["ticker"])
    enriched["stock_group"] = enriched["ticker"].map(groups).fillna("未分類")
    return enriched
